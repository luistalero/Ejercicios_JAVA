package com.ejercicios;

public class DeclaracionVariables {
    public static void main(String[] args) {

        int entero = 18;
        double decimal = 1.8;
        char letra = 'L';
        boolean VF = true;

        System.out.println("Variable de Entero: " + entero);
        System.out.println("Variable decimal: " + decimal);
        System.out.println("variable un solo caracter: " + letra);
        System.out.println("vedadero o falso: " + VF);
    }
}
